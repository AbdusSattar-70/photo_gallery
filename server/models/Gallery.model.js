const mongoose = require('mongoose');

const GallerySchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    imageUrls: {
      type: Array,
      required: true,
    },
    userRef: {
      type: String,
      required: true,
    },
  },
  { timestamps: true },
);

const Gallery = mongoose.model('Gallery', GallerySchema);

module.exports = Gallery;